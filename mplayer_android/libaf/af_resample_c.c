static __inline __int64 SMULL(int A, int B)    //64<=32*32
{
   return ((__int64)(A)*(__int64)(B));
}

#define DOUBLEPI  F2I(3.14159265358979323846*2, 28)
#define PI  F2I(3.14159265358979323846, 28) 
#define PI_OVER_2  (PI / 2) 
#define PI_16  (F2I(3.14159265358979323846, 13)>>1)
#define PI_STEP (PI_OVER_2/1024) 

static __inline int CLZ(int A)
{
   int cz=0;
   if (A==0)
      return 32;
   while(A>0){
      cz++;
      A <<= 1;
   }
   return cz;
}

static int normalization(FastFloat *a)
{
   int ret;
   int sign=0;
   int c = a->imantissa;

   if (c == 0)
   {
      a->iexp = 0;
      return 0;
   }

   if (c<0)
   {
      c = -c;
      sign = 1;
   }
   ret = CLZ(c) - 1;
   if(sign)
      c = -c;
   a->imantissa = c << ret;
   a->iexp += ret;
   return 0;
}

static int normalization_16(FastFloat *a)
{
   int ret;
   int sign=0;
   int c = a->imantissa;

   if (c == 0)
   {
      a->iexp = 0;
      return 0;
   }

   if (c<0)
   {
      c = -c;
      sign = 1;
   }
   ret = CLZ(c) - 17;
   if(ret>=0)
   {
      a->imantissa = c << ret;
      a->iexp -= ret;
   }
   else
   {
      a->imantissa = c >> (-ret);
      a->iexp += ret;
   }
   if(sign)
      a->imantissa = -a->imantissa;

   return 0;
}


static __inline FastFloat ffltMult( FastFloat ffltA, FastFloat ffltB )
{
   FastFloat ffltP;
   ffltP.imantissa = (int)(SMULL(ffltA.imantissa, ffltB.imantissa)>>32);
   ffltP.iexp = (ffltA.iexp + ffltB.iexp - 32);
   return ffltP;
}

static __inline FastFloat ffltsub_cos(FastFloat ffltA, FastFloat ffltB)
{
   FastFloat ffltP;
    
   if (ffltA.imantissa == 0)
   {
      return ffltB;
   }
   //======================//

   { 
      int tmp = ffltA.iexp-ffltB.iexp;
      
      ffltP.iexp = ffltB.iexp;

      tmp = ffltA.imantissa>>(ffltA.iexp-ffltB.iexp);

      ffltP.imantissa = tmp;
      ffltP.imantissa -= ffltB.imantissa;
   }
   normalization(&ffltP);
   return ffltP;
}

static __inline FastFloat ffltsub_sin(FastFloat ffltA, FastFloat ffltB)
{
   FastFloat ffltP;
    
   if (ffltB.imantissa == 0)
   {
      return ffltA;
   }
   //======================//

   { 
      int tmp = ffltB.iexp-ffltA.iexp;
      
      ffltP.iexp = ffltA.iexp;

      tmp = ffltB.imantissa>>tmp;

      ffltP.imantissa = ffltA.imantissa;
      ffltP.imantissa -= tmp;
   }
   normalization(&ffltP);
   return ffltP;
}


static __inline FastFloat ffltadd(FastFloat ffltA, FastFloat ffltB)
{
    FastFloat ffltP;
    
   //== oAWMAlCODEOn HA ==//  
   if (ffltA.imantissa == 0)
   {
      return ffltB;
   }
   if (ffltB.imantissa == 0)
   {
      return ffltA;
   }
   //======================//

   if (ffltA.imantissa > 0x3FFFFFFF)
   {
      ffltA.imantissa >>= 1;
      ffltA.iexp--;
   }
   if (ffltB.imantissa > 0x3FFFFFFF)
   {
      ffltB.imantissa >>= 1;
      ffltB.iexp--;
   }

   if (ffltA.iexp >= ffltB.iexp)
   { 
      int tmp = ffltA.iexp-ffltB.iexp;
      
      ffltP.iexp = ffltB.iexp;
      if(tmp >=32)
         tmp = 0;
      else
         tmp = ffltA.imantissa>>(ffltA.iexp-ffltB.iexp);

      ffltP.imantissa = ffltB.imantissa;
      ffltP.imantissa += tmp;
   }
   else
   {
      int tmp = ffltB.iexp-ffltA.iexp;
      ffltP.iexp = ffltA.iexp;
      if(tmp >=32)
         tmp = 0;
      else
         tmp = ffltB.imantissa>>(ffltB.iexp-ffltA.iexp);
      ffltP.imantissa = ffltA.imantissa + tmp;
   }
   return ffltP;
}
/** 
* Computes SIN(f), f is a fixed point number in radians. 0 <= f <= 2PI 
*/ 
FastFloat isin(int f) { 
   // If in range -pi/4 to pi/4: nothing needs to be done. 
   // otherwise, we need to get f into that range and account for 
   // sign change. 

   int sign = 1;
   //int sqr, result;
   //float out, out1;
   int tmp1, xstep;
   FastFloat y0, y1, ylen;

   if ((f > PI_OVER_2) && (f <= PI)) { 
      f = PI - f; 
   } else if ((f > PI) && (f <= (PI + PI_OVER_2))) { 
      f = f - PI; 
      sign = -1; 
   } else if (f > (PI + PI_OVER_2)) { 
      f = (PI << 1) - f; 
      sign = -1; 
   } 

   tmp1 = f/PI_STEP;
   xstep = f%PI_STEP;
   if(tmp1 == 534)
      tmp1 = tmp1;
   if(xstep == 0)
      return sin_table[tmp1];
   y0 = sin_table[tmp1]; y1 = sin_table[tmp1+1];

   //out = I2DF(y0.imantissa, y0.iexp);
   ylen = ffltsub_sin(y1,y0);
   ylen.imantissa /= PI_STEP;
   ylen.iexp -= 28;
   normalization(&ylen);
   y1.imantissa = xstep;
   y1.iexp = 28;
   y1 = ffltMult(ylen, y1);

   y0 = ffltadd(y0, y1);
   y0.imantissa = sign * y0.imantissa;
   //out = I2DF(y0.imantissa, y0.iexp);

   return y0; 
}

